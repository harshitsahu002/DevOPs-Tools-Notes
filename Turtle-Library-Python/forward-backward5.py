import turtle
t= turtle.Turtle()
t.shape("turtle")
t.forward(100) # turtle move 100 pixels forward
t.fd(100) # shortcut of the above command
t.forward(-100) # turtle will move 100 pixels backward

t.backward(100) # turtle move 100 pixels backward
t.bk(100) # shortcut of the above command
t.backward(-200) # turtle move 200 pixels forward
